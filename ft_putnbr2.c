#include "ft_printf.h"

int	ft_putnbr2(int nb, int ct)
{
	if (nb < 0)
		nb *= -1;
	if (nb >= 10)
	{
		ct += ft_putnbr2(nb / 10, ct);
		ft_putnbr2(nb % 10, ct);
	}
	else
	{
		ct += ft_putchar2(nb + 48);
	}
    return (ct);
}
